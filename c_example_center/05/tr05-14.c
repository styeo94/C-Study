#include <stdio.h>

/* for 반복문 이용하여 switch 조건문을 수행하는 프로그램 */

int main() {

	int i = 1;

	switch(i){
		case 1:
			printf("*		--- 1\n");
		case 2:
			printf("**		--- 2\n");
		case 3:
			printf("***		--- 3\n");
		case 4:
			printf("****		--- 4\n");
		case 5:
			printf("*****		--- 5\n");
			
	}

	return 0;
}
